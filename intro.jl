Idea: find the instance in the training dataset that is the *closest* to the test instance ``\mathbf{X}'``

```math
i' \leftarrow \arg\min_i \textit{dist}(\mathbf{X}^{(i)}, \mathbf{X}')
```

Then return the label ``\hat{y} = y^{i'}``.

The algorithm is called **K-nearest neighbour**.
""")

# ╔═╡ c05ba8f3-8a03-416b-bde8-c31d353070c9
