## "Hello world" ML algorithm (cont.)
The distance measure in use is called **sum of squared error** 

```math
\large
\textit{dist}(\mathbf{X}, \mathbf{X}') = \sum_{i=1:28} \sum_{j=1:28} (\mathbf{X}[i,j] - \mathbf{X}'[i,j])^2
```
or `Julia`
```julia
sum((X₁ - X₂).^2) 
```

or `Python`

```python
np.sum((X₁ - X₂)**2) # np: Numpy
```

* why *squared* error not just error?
"""

